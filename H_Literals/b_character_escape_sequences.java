package H_Literals;

public class b_character_escape_sequences {
    public static void main(String[] args) {
        // System.out.println("Hello Students !! "Good Morning.""); // error
        System.out.println("Hello Students !! \"Good Morning\" ");
        System.out.println("Hello Students !! \nGood Morning ");
        System.out.println("Hello Students !! \tGood Morning ");
        System.out.println("Hello Students !! \bGood Morning ");
        System.out.println("Hello Students !! \rGood Morning ");
        System.out.println("The \\ (Backslash) symbol is used for escape sequence.");
    }
}
